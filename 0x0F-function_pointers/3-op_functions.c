#include "function_pointers.h"
/**
 * op_add - Returns a + b.
 * @a:integer
 * @b:integer
 * Return: Sum.
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - Returns a - b.
 * @a: integer
 * @b: integer
 * Return: difference.
 */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op-mul -Return a * b.
 * @a:integer
 * @b:ineteger
 * Return: product.
 */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div -Returns a/b.
 * @a: integer
 * @b: inetger
 * Return: quotient.
 */

int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - Returns a % b.
 * @a: integer
 * @b: integer
 * Return: modulo.
 */
int op_mod(int a, int b)
{
	return (a % b);
}
